    private void JBGuardarNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBGuardarNActionPerformed
       Cliente clienteSelect = (Cliente) (JCBSeleccionDuenio.getSelectedItem());
       Mascota mascotaNueva = new Mascota();
       mascotaNueva.setIdCliente(clienteSelect.getIdCliente());
       mascotaNueva.setAlias(JTFNombre.getText());
       mascotaNueva.setEspecie(JTFEspecie.getText());
       mascotaNueva.setRaza(JTFRaza.getText());
       mascotaNueva.setPeso(Double.parseDouble(JTFPeso.getText()));
       mascotaNueva.setPesoMedio(Double.parseDouble(JTFPeso.getText()));
       mascotaNueva.setColorPelo(JTFColor.getText());
       mascotaNueva.setSexo(JTFSexo.getText());
       
       mascotaNueva.setFechaNacimiento(JDCFecha.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate());
       MascotaData md = new MascotaData();
       md.guardarMascota(mascotaNueva);
      jPanel2.setVisible(false);
       JBNuevaMascota.setEnabled(true);
       JCBSeleccionDuenio.setEnabled(false);
       JCBSeleccionMascota.setEnabled(false);
       
    }//GEN-LAST:event_JBGuardarNActionPerformed

